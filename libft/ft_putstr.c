#include "libft.h"

void	ft_putstr(char const *s)
{
	int		i;
	char	*str2;

	i = 0;
	str2 = (char *)s;
	if (str2 == NULL)
		return ;
	while (str2[i])
	{
		ft_putchar(str2[i]);
		i++;
	}
}
